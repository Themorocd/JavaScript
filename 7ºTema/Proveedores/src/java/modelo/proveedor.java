public class proveedor {
    String idproveedor,compañia,contacto,cargo,direccion,ciudad,cp,pais,telefono;

    public proveedor(String idproveedor, String compañia, String contacto, String cargo, String direccion, String ciudad, String cp, String pais, String telefono) {
        this.idproveedor = idproveedor;
        this.compañia = compañia;
        this.contacto = contacto;
        this.cargo = cargo;
        this.direccion = direccion;
        this.ciudad = ciudad;
        this.cp = cp;
        this.pais = pais;
        this.telefono = telefono;
    }

    public String getIdproveedor() {
        return idproveedor;
    }

    public void setIdproveedor(String idproveedor) {
        this.idproveedor = idproveedor;
    }

    public String getCompañia() {
        return compañia;
    }

    public void setCompañia(String compañia) {
        this.compañia = compañia;
    }

    public String getContacto() {
        return contacto;
    }

    public void setContacto(String contacto) {
        this.contacto = contacto;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public String getCp() {
        return cp;
    }

    public void setCp(String cp) {
        this.cp = cp;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    
    
}
