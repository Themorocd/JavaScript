// Esta función se llama 'marco()'
function marco() {
    // Accede al elemento con el ID "verano" en el documento HTML
    // y establece su propiedad 'checked' como true
    document.getElementById("verano").checked = true;
}

// Esta función se llama 'desmarco()'
function desmarco() {
    // Accede al elemento con el ID "verano" en el documento HTML
    // y establece su propiedad 'checked' como false
    document.getElementById("verano").checked = false;
}
