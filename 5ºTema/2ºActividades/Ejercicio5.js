// Esta función se llama 'color()'
function color() {
    // Cambia el color del texto del elemento con el ID "Text" a azul
    document.getElementById("Text").style.color = "blue";
}

// Esta función se llama 'tamaño()'
function tamaño() {
    // Cambia el tamaño de la fuente del elemento con el ID "Text" a 50 píxeles
    document.getElementById("Text").style.fontSize = "50px";
}
